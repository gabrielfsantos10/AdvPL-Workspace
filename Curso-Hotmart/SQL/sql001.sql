-- (AULA 04)
SELECT
	COUNT(B1_COD) AS QTD_PRODUTOS,
	B1_TIPO
FROM
	SB1990 SB1
WHERE
	B1_FILIAL = ' '
	AND SB1.D_E_L_E_T_ = ' '
	AND B1_TIPO = 'EM'
GROUP BY
	B1_TIPO


-- (AULA 05)
SELECT
	B1_COD,
	B1_DESC,
	B1_TIPO
FROM
	SB1990 SB1
WHERE
	B1_FILIAL = ' '
	AND SB1.D_E_L_E_T_ = ' '
ORDER BY
	B1_TIPO


-- AULA(06)
SELECT
	--C5_NUM AS NUM_PEDIDO,
	COUNT(C6_ITEM) AS QTD_VENDAS,
	B1_DESC AS DESC_PRODUTO
FROM
	SC6990 SC6
	LEFT JOIN SB1990 SB1 ON (
		C6_PRODUTO = B1_COD
	)
GROUP BY
	B1_DESC


-- AULA(07)
SELECT
	B1_COD,
	B1_DESC,
	C5_EMISSAO,
	C5_NUM,
	A1_NOME
FROM
	SB1990 SB1
	INNER JOIN SC6990 SC6 ON (
		B1_COD = C6_PRODUTO
		AND SC6.D_E_L_E_T_ = ' '
	)
	INNER JOIN SC5990 SC5 ON (
		C6_NUM = C5_NUM
		AND SC5.D_E_L_E_T_ = ' '
	)
	INNER JOIN SA1990 SA1 ON (
		C5_CLIENTE = A1_COD
		AND SA1.D_E_L_E_T_ = ' '
	)
ORDER BY
	A1_NOME


