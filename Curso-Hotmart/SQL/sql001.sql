--Seleciona dados de uma tabela
SELECT
	* 
FROM
	SBM990 
WHERE
	D_E_L_E_T_ = ' ';

--Atualiza dados de uma tabela
UPDATE
	SBM990
SET
	BM_PROORI = BM_PROORI
WHERE D_E_L_E_T_ = ' ';


